package day2;

public class DataTypes {
  public static void main(String[] args) {
    int a = 1, b = 2;
    System.out.println(a + " " + b);

    // Have to add L or l at the end of the number to make it a long or float.
    long longy = 2222222222L;

    // Float and double
    float f = 1.25f;
    System.out.println(f);

    String str = "Love you Hasna!";
    System.out.println(str);
  }
}